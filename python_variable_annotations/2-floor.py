#!/usr/bin/env python3
import math
"""
This module takes a float and returns the floor of the float.
"""


def floor(n: float) -> int:
    """
    This function takes a float and returns the floor of the float.

    Args:
     n (float): a float argument.

     Returns:
     The Floor of the float.
    """
    newval = math.floor(n)
    return newval
