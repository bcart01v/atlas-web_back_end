#!/usr/bin/env python3
"""
This module takes a float and returns a string representation
Of the float.
"""


def to_str(n: float) -> str:
    """
    This function takes a float and returns the string representation.

    Args:
        n (float): a float that will be converted to string

     Returns:
        str(n): the float converted to a string.
    """
    return str(n)
