#!/usr/bin/env python3

"""
This module contains the function add that adds two floats,
and returns their sum.
"""


def add(a: float, b: float) -> float:
    """
    This function takes two float arguments and returns their sum.

    Args:
     a (float): First float argument.
     b (float): Second float argument.

     Returns:
     float: The sum of the two floats together.
    """
    return a + b
